import { Component, OnInit } from '@angular/core';
import { ModalController } from '@ionic/angular';
import { Router } from '@angular/router';

@Component({
  selector: 'app-shoulder-tap-modal',
  templateUrl: './shoulder-tap-modal.page.html',
  styleUrls: ['./shoulder-tap-modal.page.scss'],
})
export class ShoulderTapModalPage implements OnInit {

  constructor(private mdController: ModalController, private router: Router) {}

  ngOnInit() {
  }

  closeModal()
  {
    this.mdController.dismiss();
    this.router.navigate(['/tabs/workout']);
  }

  exitExercise(){
    this.router.navigate(['/tabs/workout']);
  }

  nextExercise(){
    this.router.navigate(['/side-shoulder-tap-modal']);
  }
}
